#include <iostream>
using namespace std;
int main()
{
//while loop
  int i = 0;
  while (i < 5)
  {
    cout << i <<endl;
    i++;
  }
    
//Do while loop
    int a = 0;
      do
      {
        cout << a << endl;
        a++;
      }
      while (a < 5);
    
//for loop
      for (int b = 0; b < 5; b++)
      {
        cout << b <<endl;
      }
    
  return 0;
}
